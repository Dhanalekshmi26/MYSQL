-- Create the EXAM database
CREATE DATABASE EXAM;

-- Use the EXAM database
USE EXAM;

-- Create USERS table
CREATE TABLE USERS (
    id INT PRIMARY KEY,
    NAME VARCHAR(50),
    DOB DATE,
    SCORE FLOAT,
    IS_ACTIVE BOOLEAN
);

