-- Query: Top 5 scores >= 70
SELECT NAME, SCORE FROM USERS 
WHERE SCORE >= 70 
ORDER BY SCORE DESC 
LIMIT 5;

-- Query: View highest scorer
SELECT NAME, SCORE FROM USERS 
WHERE SCORE >= 70 
ORDER BY SCORE DESC 
LIMIT 1;

